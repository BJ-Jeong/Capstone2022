package com.example.capstone2022;

public class ContactModel {

    //변수 생성
    String name, number;

    //getter 와 setter 설정

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number = number;
    }

}
